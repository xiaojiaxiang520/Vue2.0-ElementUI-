<template>
    <div class="login-wrap">
        <div class="ms-login">
            <div class="ms-title">后台管理系统</div>
            <el-form :model="param" :rules="rules" ref="login" label-width="0px" class="ms-content">
                <el-form-item prop="username">
                    <el-input v-model="param.username" placeholder="username">
                        <el-button slot="prepend" icon="el-icon-lx-people"></el-button>
                    </el-input>
                </el-form-item>
                <el-form-item prop="password">
                    <el-input
                        type="password"
                        placeholder="password"
                        v-model="param.password"
                        @keyup.enter.native="submitGet()"
                    >
                        <el-button slot="prepend" icon="el-icon-lx-lock"></el-button>
                    </el-input>
                </el-form-item>
                <div class="login-btn">
                    <el-button type="primary" @click="submitGet()">登录</el-button>
                </div>
                <p class="login-tips">Tips : 用户名和密码随便填。</p>
            </el-form>
        </div>
    </div>
</template>

<script>
export default {
    data: function() {
        return {
            param: {
                username: '',
                password: '',
            },
            rules: {
                username: [{ required: true, message: '请输入用户名', trigger: 'blur' }],
                password: [{ required: true, message: '请输入密码', trigger: 'blur' }],
            },
        };
    },
    methods: {
        submitGet(){
            if(this.param.username===''||this.param.password===''||this.param.password===null||this.param.username===null)
            {
              this.tishi("账号密码不能为空.");
              return;
            }
            this.rq.requests.get('/zz/admin/login',{
              params:{
                username:this.param.username,
                password:this.param.password
              }
              ,
            }).then(function (response){
              //获得到数据

              //登录成功
              if(response.data.code === 200)
              {
                localStorage.setItem('token',response.data.data.token)
                localStorage.setItem('ms_username', response.data.data.user.username);

                //设置当前路由的地址为/
                this.$router.push('/admin');
              }else {
                this.$message.error('账号或密码错误，请重新登录！');
              }
            }.bind(this)).catch(function (error){
              //
              console.log(error)
            })
        },
        tishi(data){
          this.$message({
            message: data,
            type: 'warning'
          });
        },
    },
};
</script>

